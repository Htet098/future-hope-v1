                <h3 class="py-2 bg-warning bg-opacity-50 mt-3 fw-bold fs-5">1. Student-Centered Discussions</h3>
                <div class=" m-3 ">I admit that I do enjoy being the "sage on the stage" in my classroom, but I realize that this does little to engage my students in deep thinking. I want my students to be at the center of their learning. Previously, when we read a book
                    or short story, I'd ask questions to the whole class. Inevitably, the same five or six students would answer the questions, and I'd assume everyone understood. Now, I create small groups of three or four students, and they answer the
                    questions in those small groups first, ensuring all students have an opportunity to participate. I've seen an increase in comprehension, speaking and listening skills, and test scores. Implementing student-centered discussions is fairly
                    easy to doâ€”just create thought-provoking questions that dive deep into the content. The rewards are definitely worth it.
                </div>
            </div>
            <div class="col-lg-5 shadow-sm border-2 m-4   overflow-auto " style="height:25rem;">
                <h3 class="py-2 bg-warning bg-opacity-50 mt-3 fw-bold fs-5">2. Making Connections</h3>
                <div class=" m-3  ">It is absolutely essential that the teaching methods you use help students make connections to their learning. Real-life connections make learning interesting and relevant for students. When I teach Macbeth, for example, I have my students
                    focus on the idea of setting goals. They examine how ambition can be both positive and negative, and I have them study recent world leaders to help them understand the real-life implications of this concept. Further reading: Create
                    an Engaged and Positive Classroom Culture A history teacher at my school conducts a G20 conference in his classroom, with students representing countries and focusing on specific issues. The students' goal is to explore the problems
                    of each specific region and form relationships with other nations in order to address them. This helps students personally identify with international issues in a way they never could before. In physics class, students build their
                    own physical models to apply theory to real life. Examine your curriculum and see what relatable applications you can bring into the classroom. Students will be much more engaged, and those real-world connections will help students
                    understand the content and theories you're teaching much better than by simply reading a textbook.
                </div>
            </div>
            <div class="col-lg-5 shadow-sm border-2 m-4   overflow-auto" style="height:25rem;">
                <h3 class="py-2 bg-warning bg-opacity-50 mt-3 fw-bold fs-5">3. Increased Autonomy</h3>
                <div class=" m-3 ">After reading the research on student autonomy, I wanted to increase student independence in my classroom. This year, I've allowed my students to pick from several prompts when writing an essay. Because it's important to tie learning to
                    students' personal interests, I permit students to choose their own topics for research papers and to choose which projects they want to explore. I make sure to still provide my students with clear learning goals, but they are able
                    to make their own choices within that framework. Autonomy helps engage and empower my students, and it enables them to have a voice in their learning.
                </div>
            </div>
            <div class="col-lg-5 shadow-sm border-2 m-4  overflow-auto " style="height:25rem;">
                <h3 class="py-2 bg-warning bg-opacity-50 mt-3 fw-bold fs-5">4. Building Relationships</h3>
                <div class=" m-3 ">Building relationships in the classroom is extremely powerful for students' behavioral and academic success. Teachers can help build relationships by meeting with students during office hours and creating team-building projects like student
                    videos. Further reading: Building a Joyful Classroom Attending student sporting events and after-school activities also provides a great launching pad for discussion. Finally, I like to have a few minutes of "real talk" each week in
                    my classroom for students to discuss topics that are important to them, and that helps break down walls and build solidarity in the classroom.
                </div>
            </div>
            <div class="col-lg-5 shadow-sm border-2 m-4  overflow-auto " style="height:25rem;">
                <h3 class="py-2 bg-warning bg-opacity-50 mt-3 fw-bold fs-5">5. A Focus on Literacy</h3>
                <div class=" m-3 ">I'm always trying to improve my students' reading. Providing students with reading material that interests them and helps them to understand the joys of reading for pleasure. I make sure my classroom is well-stocked with books I know young
                    adults enjoy. In addition, if I see a strong op-ed piece online or in the newspaper, I'll share it with my students. I've noticed that my students now come to me when they read something compelling and thought-provoking, and they've
                    become stronger readers and writers as a result. These are the best teaching methods I've utilized this year. How about you? What are the best methods and strategies that have worked in your classroom? Comment below!
